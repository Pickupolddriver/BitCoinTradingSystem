    public void populateUser(){
        int rowCount = TblUserAccount.getRowCount();
        DefaultTableModel model = (DefaultTableModel) TblUserAccount.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (Organization or: enterprise.getOrganizationDirectory().getOrganizationList()){
            for (UserAccount ua: or.getUserAccountDirectory().getUserAccountList()){
                Object row[] = new Object[2];
                row[0] = ua.getUsername();
                row[1] = ua.getRole();
                model.addRow(row);
            }
        }
    }
    
