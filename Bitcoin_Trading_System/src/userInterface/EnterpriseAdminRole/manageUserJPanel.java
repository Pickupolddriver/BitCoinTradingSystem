    public void populateEmployeeCmb(Organization or){
        CmbEmployee.removeAllItems();
        for (Employee e: or.getEmployeeDirectory().getEmployeeList()){
            CmbEmployee.addItem(e);
        }
    }
    public void populatRoleCmb(Organization or){
        CmbRoleType.removeAllItems();
        for (Role r: or.getSupportedRole()){
            CmbRoleType.addItem(r);
        }
    }

